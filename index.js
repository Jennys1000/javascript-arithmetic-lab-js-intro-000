function add (a, b) {
number = a + b
return number
}
function subtract (a, b) {
  number = a - b
  return number
}
function multiply (a, b) {
number = a * b
return number
}

function divide (a, b) {
number = a / b
return number
}

function increment(a) {
   a += 1
  return a
}

function decrement(a) {
  a -= 1
  return a
}
function makeInt(a) {
   parseInt(a, 10)
  return a
}
function preserveDecimal(a) {
  parseFloat(a, 10)
  return a
}
